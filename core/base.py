def get_initial_green_energy_params(group_id):
    """获取指定分组的绿电企业初始参数"""
    if group_id not in GREEN_ENERGY_GROUPS:
        raise ValueError(f"无效的分组ID: {group_id}")

    params = GREEN_ENERGY_GROUPS[group_id].copy()
    # 移除分组名称和计数信息，只保留数值参数
    params.pop('name', None)
    params.pop('count', None)

    return params


def get_scenario_target(scenario_id):
    """获取指定情景的政策目标值"""
    if scenario_id not in SCENARIO_TARGETS:
        raise ValueError(f"无效的情景ID: {scenario_id}")

    return SCENARIO_TARGETS[scenario_id].copy()


def get_scenario_name(scenario_id):
    """获取指定情景的名称"""
    return SCENARIO_NAMES.get(scenario_id, f"未知情景{scenario_id}")


